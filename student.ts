export class Student {
	name: string;
	code: number;
	id: number;
	age: number;
	addres: string;
	number: number;

	constructor(
		name: string,
		code: number,
		id: number,
		age: number,
		addres: string,
		number: number
	) {
		this.name = name;
		this.code = code;
		this.id = id;
		this.age = age;
		this.addres = addres;
		this.number = number;
	}
}
